function setup() {
  createCanvas(xSize + 1, ySize + 1);
  for (let i = 0; i <= xSize; i += pixSize) {
    line(i, 0, i, ySize);
  }
  for (let i = 0; i <= ySize; i += pixSize) {
    line(0, i, xSize, i);
  }
  noStroke();
  fill(color(255, 0, 0));
}

let prevX;
let prevY;
function draw() {
  if (mouseIsPressed) {
    if (mouseX <= xSize || mouseY <= ySize) {
      let x = Math.floor(mouseX / scale);
      let y = Math.floor(mouseY / scale);
      if (
        y >= 0 &&
        y < Ypix &&
        x >= 0 &&
        x < Xpix
      ) {
        if (x != prevX || y !== prevY) {
          prevX = x;
          prevY = y;

          if (arr[y][x] == 0) {
            fill(color(255, 0, 0));
            arr[y][x] = 1;
          } else {
            arr[y][x] = 0;
            fill(color(255, 255, 255));
          }
          x *= pixSize;
          y *= pixSize;
          rect(x + 1, y + 1, pixSize - 1, pixSize - 1);
        }
      }
    }
  }
}
