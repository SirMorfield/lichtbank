function setup() {
  createCanvas(xSize + 1, ySize + 1);
  // background(230);
  // rectMode(CENTER);
  for (let i = 0; i <= xSize; i += pixSize) {
    line(i, 0, i, ySize);
  }
  for (let i = 0; i <= ySize; i += pixSize) {
    line(0, i, xSize, i);
  }
  noStroke();
  fill(color(255, 0, 0));
}

let prevX;
let prevY;
function draw() {
  if (mouseIsPressed) {
    if (mouseX <= xSize || mouseY <= ySize) {
      let x = Math.floor(mouseX / scale);
      let y = Math.floor(mouseY / scale);
      if (
        y >= 0 &&
        y <= 72 &&
        x >= 0 &&
        x <= 47
      ) {
        if (x != prevX || y !== prevY) {
          console.log(x, y);
          prevX = x;
          prevY = y;

          if (arr[y][x] == 0) {
            fill(color(255, 0, 0));
            arr[y][x] = 1;
          } else {
            arr[y][x] = 0;
            fill(color(255, 255, 255));
          }
          x *= pixSize;
          y *= pixSize;
          rect(x + 1, y + 1, pixSize - 1, pixSize - 1);
        }
      }
    }
  }
}
